int	ft_iterative_factorial(int nb)
{
	int f;

	f = 1;
	if (nb < 0 || nb > 12)
	{
		return (0);
	}
	if (nb == 0 || nb == 1)
	{
		return (f);
	}
	while (nb > 1)
	{
		f = f * nb;
		nb--;
	}
	return (f);
}
